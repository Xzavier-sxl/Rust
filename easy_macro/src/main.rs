// 定义一个声明宏，用于计算两个数的和
macro_rules! add {
    // 宏模式，匹配两个表达式
    ($x:expr, $y:expr) => {
        // 宏展开，生成相应的代码
        $x + $y
    };
}

fn main() {
    let result = add!(2, 3); // 使用宏进行计算
    println!("The result is: {}", result);
}
/*在上面的例子中，我们定义了一个名为add!的声明宏，它可以接受两个表达式作为参数。当我们在代码中使用add!宏时，它将被展开为相应的代码，这里是将两个表达式相加并返回结果。在main函数中，我们使用了add!宏来计算2和3的和，并将结果打印出来*/
